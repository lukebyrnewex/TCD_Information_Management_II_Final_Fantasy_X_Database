  location_id           int UNIQUE,
  airship_X_coordinate  int NOT NULL,
  airship_Y_coordinate  int NOT NULL,
  PRIMARY KEY(location_id),
  FOREIGN KEY(location_id) REFERENCES location(location_id),
  CHECK (airship_X_coordinate >= 10 AND airship_X_coordinate <= 80),
  CHECK (airship_Y_coordinate >= 30 AND airship_Y_coordinate <= 80)
